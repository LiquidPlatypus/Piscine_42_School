int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	x;

	x = 0;
	while (s1[x] && s1[x] == s2[x] && n > 0)
	{
		x++;
		n--;
	}
	if (n == 0)
		return (0);
	return (s1[x] - s2[x]);
}
