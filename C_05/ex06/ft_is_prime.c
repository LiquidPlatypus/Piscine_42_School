int	ft_is_prime(int nb)
{
	int	x;

	x = 2;
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (0);
	while (x <= (nb / 2))
	{
		if (!(nb % x))
			return (0);
		x++;
	}
	return (1);
}
